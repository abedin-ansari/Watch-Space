export function generateRandomMessage() {
  const messageLength = Math.floor(Math.random() * 4) + 4; // 4–7 words
  let message = [];

  for (let i = 0; i < messageLength; i++) {
    message.push(words[Math.floor(Math.random() * words.length)]);
  }

  // Capitalize the first letter & add a period at the end
  const finalMessage =
    message.join(" ").charAt(0).toUpperCase() +
    message.join(" ").slice(1) +
    ".";

  return finalMessage;
}
